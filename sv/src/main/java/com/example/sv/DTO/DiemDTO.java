package com.example.sv.DTO;

public class DiemDTO {
    private Long id;
    private Long lopTC;
    private Long sinhVien;
    private Float cc;
    private Float gk;
    private Float thi;

    public Long getId() {
        return this.id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getLopTC() {
        return this.lopTC;
    }

    public void setLopTC(Long lopTC) {
        this.lopTC = lopTC;
    }

    public Long getSinhVien() {
        return this.sinhVien;
    }

    public void setSinhVien(Long sinhVien) {
        this.sinhVien = sinhVien;
    }

    public Float getCc() {
        return this.cc;
    }

    public void setCc(Float cc) {
        this.cc = cc;
    }

    public Float getGk() {
        return this.gk;
    }

    public void setGk(Float gk) {
        this.gk = gk;
    }

    public Float getThi() {
        return this.thi;
    }

    public void setThi(Float thi) {
        this.thi = thi;
    }

}
